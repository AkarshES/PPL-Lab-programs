class class_res
{
	public static void main(String [] args) 
	{
		System.out.println(add());
		System.out.println(add(5));
		System.out.println(add(5,6));
	}
	public static int add()
	{
		return(0);
	}
	public static int add(int i)
	{
		return(i);
	}
	public static int add(int i,int j)
	{
		return(i+j);
	}
}
